long long	swap_long_long(long long ll)
{
	return (((ll & 0xFF00FF00FF00FF00) >> 8) | ((ll & 0xFF00FF00FF00FF) << 8));
}

short		swap_short(short s)
{
	return (((s & 0xFF) << 8) | (s >> 8));
}
