#include "libft.h"

int		ft_strnword(char *s1, char *s2)
{
	int		occ;
	int		i;
	int		j;

	occ = 0;
	j = 0;
	if (!s1 && !s2)
		return (0);
	while (s1[j++])
	{
		i = 0;
		if (s1[j] == s2[i])
		{
			while (s1[j] == s2[i] && s2[i++])
				j++;
			j--;
		}
		if (!s2[i])
		{
			i = 0;
			occ++;
		}
	}
	return (occ);
}
